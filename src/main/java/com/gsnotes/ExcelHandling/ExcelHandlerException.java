package com.gsnotes.ExcelHandling;

public class ExcelHandlerException  extends  Exception{

	public ExcelHandlerException() {
		super();
	}

	public ExcelHandlerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ExcelHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExcelHandlerException(String message) {
		super(message);
	}

	public ExcelHandlerException(Throwable cause) {
		super(cause);
	}
	
	

}
